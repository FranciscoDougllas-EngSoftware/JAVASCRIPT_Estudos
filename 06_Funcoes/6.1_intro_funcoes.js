//O que é uma função?
//Uma função é um bloco de código que pode ser chamado em qualquer lugar do seu programa.

//Declarando uma função

function somar(a, b){
    console.log(`A soma de ${a} e ${b} é  = ${a + b}`);
}

somar(2, 3); // 5
somar(10, 20); // 30
somar(100, 200); // 300