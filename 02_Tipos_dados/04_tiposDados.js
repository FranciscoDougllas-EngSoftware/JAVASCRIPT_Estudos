//Tipos de Dados em JavaScript
// 1. Tipos primitivos
// 2. Tipos de referência

// 1. Tipos primitivos
// - Number: Números inteiros e de ponto flutuante
// - String: Cadeias de caracteres (texto)
// - Boolean: Valores lógicos (true ou false)
// - Undefined: Variável declarada, mas não inicializada
// - Null: Ausência intencional de valor
// - Symbol: Representa um valor único e imutável (ES6)
// - BigInt: Representa números inteiros grandes (ES11)
// - Object: Estruturas de dados complexas (não é primitivo, mas é importante)
// - Array: Estruturas de dados que armazenam listas de valores (não é primitivo, mas é importante)
// - Function: Estruturas de dados que armazenam funções (não é primitivo, mas é importante)
// - Date: Estruturas de dados que armazenam datas (não é primitivo, mas é importante)
// - RegExp: Estruturas de dados que armazenam expressões regulares (não é primitivo, mas é importante)
// - Map: Estruturas de dados que armazenam pares chave-valor (não é primitivo, mas é importante)
// - Set: Estruturas de dados que armazenam valores únicos (não é primitivo, mas é importante)
// - WeakMap: Estruturas de dados que armazenam pares chave-valor com referências fracas (não é primitivo, mas é importante)
// - WeakSet: Estruturas de dados que armazenam valores únicos com referências fracas (não é primitivo, mas é importante)
// - ArrayBuffer: Estruturas de dados que armazenam dados binários (não é primitivo, mas é importante)
// - TypedArray: Estruturas de dados que armazenam dados binários com tipos específicos (não é primitivo, mas é importante)
// - DataView: Estruturas de dados que permitem acesso a dados binários (não é primitivo, mas é importante)
// - Promise: Estruturas de dados que representam operações assíncronas (não é primitivo, mas é importante)
// - AsyncFunction: Estruturas de dados que representam funções assíncronas (não é primitivo, mas é importante)
// - GeneratorFunction: Estruturas de dados que representam funções geradoras (não é primitivo, mas é importante)

