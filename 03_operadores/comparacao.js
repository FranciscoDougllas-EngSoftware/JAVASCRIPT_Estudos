//Operadores de COmparacao
// == igual
// === igual e do mesmo tipo
// != diferente
// !== diferente e do mesmo tipo
// > maior
// < menor
// >= maior ou igual
// <= menor ou igual

// Exemplo de uso dos operadores de comparação
let a = 10;
let b = 20;

console.log(a == b); // false
console.log(a != b); // true
console.log(a === b); // false
console.log(a !== b); // true
console.log(a > b); // false
console.log(a < b); // true
console.log(a >= b); // false
console.log(a <= b); // true
