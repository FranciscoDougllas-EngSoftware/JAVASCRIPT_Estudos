//Operadores de Atribuição
// Os operadores de atribuição são usados para atribuir valores a variáveis. O operador de atribuição mais comum é o sinal de igual (=), que atribui o valor à variável à esquerda. Existem também operadores de atribuição compostos, que combinam operações aritméticas com atribuição. Aqui estão alguns exemplos:

// Atribuição simples
let x = 10; // Atribui o valor 10 à variável x
console.log(x); // Saída: 10