//OPeradores de incremento e decremento
// Os operadores de incremento (++) e decremento (--) são usados para aumentar ou diminuir o valor de uma variável em 1. Eles podem ser usados em duas formas: prefixo e sufixo. Aqui estão alguns exemplos:

let numero = 10;
console.log(numero); // Saída: 10

numero++; // Incrementa o valor de numero em 1 (sufixo)
console.log(numero); // Saída: 11

numero--; // Decrementa o valor de numero em 1 (sufixo)
console.log(numero); // Saída: 10

