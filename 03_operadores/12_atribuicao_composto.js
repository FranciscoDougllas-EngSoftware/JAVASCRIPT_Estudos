//Operadores de Atribuicao compostos
// Os operadores de atribuição compostos combinam uma operação aritmética com a atribuição. Eles são usados para atualizar o valor de uma variável com base em seu valor atual. Aqui estão alguns exemplos:
// Temos os seguintes operadores de atribuição compostos:
// +=, -=, *=, /=, %=, **=

let numero = 25;
console.log(numero); // Saída: 25

numero += 5; // Adiciona 5 ao valor de numero
console.log(numero); // Saída: 30

numero -= 3; // Subtrai 3 do valor de numero
console.log(numero); // Saída: 27

numero *= 2; // Multiplica o valor de numero por 2
console.log(numero); // Saída: 54

numero /= 3; // Divide o valor de numero por 3
console.log(numero); // Saída: 18

numero %= 4; // Calcula o resto da divisão de numero por 4
console.log(numero); // Saída: 2

numero **= 3; // Eleva o valor de numero à potência de 3
console.log(numero); // Saída: 8
// O operador **= é usado para elevar o valor de uma variável a uma potência. Neste caso, elevamos o valor de numero à potência de 3, resultando em 8.