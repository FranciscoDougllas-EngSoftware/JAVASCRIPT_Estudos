//OPeradores Matematicos em JavaScript

//Os operadores matemáticos em JavaScript são usados para realizar operações aritméticas. Aqui estão os principais operadores matemáticos disponíveis:
//1. Adição (+): Soma dois números.
//2. Subtração (-): Subtrai um número de outro.
//3. Multiplicação (*): Multiplica dois números.
//4. Divisão (/): Divide um número por outro.
//5. Módulo (%): Retorna o resto da divisão de um número por outro.
//6. Exponenciação (**): Eleva um número a uma potência.

let a = 10;
let b = 5;
let c = 2;
let d = 3;

console.log(a + b); // Adição: 10 + 5 = 15
console.log(a - b); // Subtração: 10 - 5 = 5
console.log(a * b); // Multiplicação: 10 * 5 = 50
console.log(a / b); // Divisão: 10 / 5 = 2
console.log(a % b); // Módulo: 10 % 5 = 0 (resto da divisão)
console.log(a ** c); // Exponenciação: 10 ** 2 = 100 (10 elevado a 2)
console.log(a ** d); // Exponenciação: 10 ** 3 = 1000 (10 elevado a 3)