//Operadores Lógicos
// && e (and)
// || ou (or)
// ! negação (not)

// Exemplo de uso dos operadores lógicos
let a = true;
let b = false;

let c = true;
let d = false;

console.log(a && b); // false (a e b)
console.log(a || b); // true (a ou b)
console.log(!a); // false (não a)
console.log(a && c); // true (a e c)
console.log(a || d); // true (a ou d)
console.log(!b); // true (não b)